package exercitii;

public class Problema1 {
    public static void main(String[] args) {
        byte myByte =12;
        short myShort = 500;
        int myInt = 14;
        long myLong=15L;
        double myDouble = 9.34D;
        float myFloat=12.14F;
        char myChar='R';
        boolean myBoolean = true;
        System.out.println(myByte);
        System.out.println("Valoare de mai sus este de tip byte si foloseste 8 biti");
        System.out.println(myShort);
        System.out.println("Valoare de mai sus este de tip short si foloseste 16 biti");
        System.out.println(myInt);
        System.out.println("Valoare de mai sus este de tip int si foloseste 32 biti");
        System.out.println(myLong);
        System.out.println("Valoare de mai sus este de tip long si foloseste 64 biti");
        System.out.println(myFloat);
        System.out.println("Valoare de mai sus este de tip float si foloseste 32 biti");
        System.out.println(myDouble);
        System.out.println("Valoare de mai sus este de tip double si foloseste 64 biti");
        System.out.println(myChar);
        System.out.println("Valoare de mai sus este de tip char si foloseste 16 biti");//https://study.com/academy/lesson/java-char-data-type.html
        System.out.println(myBoolean);
        System.out.println("Valoare de mai sus este de tip short si foloseste 1 bit");
    }
}
